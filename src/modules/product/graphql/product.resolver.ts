  @Mutation(() => ProductResponse<Product>)
  async createOrUpdateProduct(
    @Args("data", { type: () => CreateOrUpdateProductDto })
    data: CreateOrUpdateProductDto
  ): Promise<ProductResponse<Product>> {
    if (data.id) {
      const existingProduct = await this.service.findById(data.id);
      if (existingProduct) {
        return this.commandBus.execute(
          new UpdateProductCommand(data, {
            instance: data,
            metadata: {
              initiatedBy:
                (data.input as CreateProductDto | UpdateProductDto).createdBy ||
                'system',
              correlationId: data.id,
            },
          })
        );
      }
    }
    return this.commandBus.execute(
      new CreateProductCommand(data, {
        instance: data,
        metadata: {
          initiatedBy:
            (data.input as CreateProductDto | UpdateProductDto).createdBy ||
            'system',
          correlationId: data.id || uuidv4(),
        },
      })
    );
  }


